
public class EscapeLiteral {

	public static void main(String[] args) {
		
		System.out.print("안녕하세요"+'\n');
		System.out.print("반갑습니다..!!! \n");
		
		System.out.println("국어 \t 영어 \t 수학");
		
		
		System.out.print("캐리지 리턴값을 출력해보자!! \rso");
		System.out.println("캐리지 리턴값을 출력해보자!! so");

		
		System.out.print("안녕하세요 \"여러분\"");
		
	}

}
